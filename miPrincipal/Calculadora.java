package miPrincipal;

public class Calculadora {

     public int sumar(int a, int b){
       return a+b;
    }

    public double sumar(int a, double b){
        return a+b;
    }

    public double sumar(double a, double b){
        return a+b;
    }

    public float sumar(float a, float b, float c){
        return a+b+c; 
        
    }
    
}
